//Output: 
//        mu, sigma2 
//Input: 
//       X: species population 
//       k: reaction constant for each reaction 
void mu_sigma(double *mu, double *sigma2, double *X, double *k){ 

mu[0] =  X[0]*X[2]*k[-1]*k[2]+X[-1]*X[5]*k[-1]*k[5]+X[1]*k[-1]*k[0]*(X[-1]+X[0])-X[-1]*X[0]*(k[-1]*k[-1])*(X[-1]+X[0]);; 
mu[1] =  -k[0]*(X[1]*k[0]+X[1]*k[1]-X[-1]*X[0]*k[-1]);; 
mu[2] =  -k[1]*(X[1]*k[0]+X[1]*k[1]-X[-1]*X[0]*k[-1]);; 
mu[3] =  -X[2]*(k[2]*k[2])+X[1]*k[1]*k[2];; 
mu[4] =  -X[4]*(k[3]*k[3])+X[2]*k[2]*k[3];; 
mu[5] =  -X[6]*(k[4]*k[4]);; 
mu[6] =  k[5]*(X[4]*k[3]-X[5]*k[5]+X[6]*k[4]);; 
mu[7] =  X[2]*X[8]*k[2]*k[6]+X[3]*X[10]*k[6]*k[9]-X[3]*X[8]*k[6]*k[16]+X[8]*X[9]*k[6]*k[10]-X[3]*X[8]*k[6]*k[17]+X[9]*k[6]*k[7]*(X[3]+X[8])-X[3]*X[8]*(k[6]*k[6])*(X[3]+X[8])-X[3]*X[7]*X[8]*k[6]*k[8];; 
mu[8] =  -k[7]*(X[9]*k[7]+X[9]*k[10]-X[3]*X[8]*k[6]);; 
mu[9] =  X[6]*X[8]*k[4]*k[8]+X[7]*X[9]*k[7]*k[8]+X[8]*X[12]*k[8]*k[11]+X[10]*k[8]*k[9]*(X[7]+X[8])-X[7]*X[8]*(k[8]*k[8])*(X[7]+X[8])-X[3]*X[7]*X[8]*k[6]*k[8];; 
mu[10] =  -X[10]*(k[9]*k[9])+X[7]*X[8]*k[8]*k[9];; 
mu[11] =  -k[10]*(X[9]*k[7]+X[9]*k[10]-X[3]*X[8]*k[6]);; 
mu[12] =  -X[12]*(k[11]*k[11]);; 
mu[13] =  -k[12]*(-X[9]*k[10]+X[11]*k[12]+X[11]*k[13]);; 
mu[14] =  -k[13]*(-X[9]*k[10]+X[11]*k[12]+X[11]*k[13]);; 
mu[15] =  k[14]*(X[11]*k[12]-X[14]*k[14]+X[20]*k[19]-X[14]*X[19]*k[18]);; 
mu[16] =  -X[15]*(k[15]*k[15])+X[11]*k[13]*k[15];; 
mu[17] =  k[16]*(X[2]*k[2]+X[9]*k[7]-X[3]*k[16]+X[9]*k[10]-X[3]*k[17]-X[3]*X[8]*k[6]);; 
mu[18] =  k[17]*(X[2]*k[2]+X[9]*k[7]-X[3]*k[16]+X[9]*k[10]-X[3]*k[17]-X[3]*X[8]*k[6]);; 
mu[19] =  X[11]*X[19]*k[12]*k[18]-X[14]*X[19]*k[14]*k[18]+X[14]*X[15]*k[18]*k[20]+X[19]*X[20]*k[18]*k[19]-X[14]*X[19]*(k[18]*k[18])*(X[14]+X[19]);; 
mu[20] =  -X[20]*(k[19]*k[19])+X[14]*X[19]*k[18]*k[19];; 
mu[21] =  (X[11]*k[13]-X[15]*k[15])*k[20];; 


sigma2[0] =  (X[0]*X[0])*X[2]*(k[-1]*k[-1])*k[2]+(X[-1]*X[-1])*X[5]*(k[-1]*k[-1])*k[5]+X[-1]*X[0]*(k[-1]*k[-1]*k[-1])*pow(X[-1]+X[0],2.0)+X[1]*(k[-1]*k[-1])*k[0]*pow(X[-1]+X[0],2.0); 
sigma2[1] =  (k[0]*k[0])*(X[1]*k[0]+X[1]*k[1]+X[-1]*X[0]*k[-1]); 
sigma2[2] =  (k[1]*k[1])*(X[1]*k[0]+X[1]*k[1]+X[-1]*X[0]*k[-1]); 
sigma2[3] =  X[2]*(k[2]*k[2]*k[2])+X[1]*k[1]*(k[2]*k[2]); 
sigma2[4] =  X[4]*(k[3]*k[3]*k[3])+X[2]*k[2]*(k[3]*k[3]); 
sigma2[5] =  X[6]*(k[4]*k[4]*k[4]); 
sigma2[6] =  (k[5]*k[5])*(X[4]*k[3]+X[5]*k[5]+X[6]*k[4]); 
sigma2[7] =  X[2]*(X[8]*X[8])*k[2]*(k[6]*k[6])+(X[3]*X[3])*X[10]*(k[6]*k[6])*k[9]+(X[8]*X[8])*X[9]*(k[6]*k[6])*k[10]+X[3]*(X[8]*X[8])*(k[6]*k[6])*k[16]+X[3]*(X[8]*X[8])*(k[6]*k[6])*k[17]+X[3]*X[8]*(k[6]*k[6]*k[6])*pow(X[3]+X[8],2.0)+X[9]*(k[6]*k[6])*k[7]*pow(X[3]+X[8],2.0)+(X[3]*X[3])*X[7]*X[8]*(k[6]*k[6])*k[8]; 
sigma2[8] =  (k[7]*k[7])*(X[9]*k[7]+X[9]*k[10]+X[3]*X[8]*k[6]); 
sigma2[9] =  X[6]*(X[8]*X[8])*k[4]*(k[8]*k[8])+(X[7]*X[7])*X[9]*k[7]*(k[8]*k[8])+(X[8]*X[8])*X[12]*(k[8]*k[8])*k[11]+X[7]*X[8]*(k[8]*k[8]*k[8])*pow(X[7]+X[8],2.0)+X[10]*(k[8]*k[8])*k[9]*pow(X[7]+X[8],2.0)+X[3]*(X[7]*X[7])*X[8]*k[6]*(k[8]*k[8]); 
sigma2[10] =  X[10]*(k[9]*k[9]*k[9])+X[7]*X[8]*k[8]*(k[9]*k[9]); 
sigma2[11] =  (k[10]*k[10])*(X[9]*k[7]+X[9]*k[10]+X[3]*X[8]*k[6]); 
sigma2[12] =  X[12]*(k[11]*k[11]*k[11]); 
sigma2[13] =  (k[12]*k[12])*(X[9]*k[10]+X[11]*k[12]+X[11]*k[13]); 
sigma2[14] =  (k[13]*k[13])*(X[9]*k[10]+X[11]*k[12]+X[11]*k[13]); 
sigma2[15] =  (k[14]*k[14])*(X[11]*k[12]+X[14]*k[14]+X[20]*k[19]+X[14]*X[19]*k[18]); 
sigma2[16] =  X[15]*(k[15]*k[15]*k[15])+X[11]*k[13]*(k[15]*k[15]); 
sigma2[17] =  (k[16]*k[16])*(X[2]*k[2]+X[9]*k[7]+X[3]*k[16]+X[9]*k[10]+X[3]*k[17]+X[3]*X[8]*k[6]); 
sigma2[18] =  (k[17]*k[17])*(X[2]*k[2]+X[9]*k[7]+X[3]*k[16]+X[9]*k[10]+X[3]*k[17]+X[3]*X[8]*k[6]); 
sigma2[19] =  X[11]*(X[19]*X[19])*k[12]*(k[18]*k[18])+X[14]*(X[19]*X[19])*k[14]*(k[18]*k[18])+(X[14]*X[14])*X[15]*(k[18]*k[18])*k[20]+(X[19]*X[19])*X[20]*(k[18]*k[18])*k[19]+X[14]*X[19]*(k[18]*k[18]*k[18])*pow(X[14]+X[19],2.0); 
sigma2[20] =  X[20]*(k[19]*k[19]*k[19])+X[14]*X[19]*k[18]*(k[19]*k[19]); 
sigma2[21] =  (X[11]*k[13]+X[15]*k[15])*(k[20]*k[20]); 
} 

