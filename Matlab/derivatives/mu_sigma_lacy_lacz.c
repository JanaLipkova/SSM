//        mu, sigma2 
//Input: 
//       X: species population 
//       k: reaction constant for each reaction 
void mu_sigma(double *mu, double *sigma2, double *X, double *k){ 

mu[0] =  X[1]*X[3]*k[0]*k[3]+X[0]*X[6]*k[0]*k[6]+X[2]*k[0]*k[1]*(X[0]+X[1])-X[0]*X[1]*(k[0]*k[0])*(X[0]+X[1]); 
mu[1] =  -k[1]*(X[2]*k[1]+X[2]*k[2]-X[0]*X[1]*k[0]); 
mu[2] =  -k[2]*(X[2]*k[1]+X[2]*k[2]-X[0]*X[1]*k[0]); 
mu[3] =  -X[3]*(k[3]*k[3])+X[2]*k[2]*k[3]; 
mu[4] =  -X[5]*(k[4]*k[4])+X[3]*k[3]*k[4]; 
mu[5] =  -X[7]*(k[5]*k[5]); 
mu[6] =  k[6]*(X[5]*k[4]-X[6]*k[6]+X[7]*k[5]); 
mu[7] =  X[3]*X[9]*k[3]*k[7]+X[4]*X[11]*k[7]*k[10]-X[4]*X[9]*k[7]*k[17]+X[9]*X[10]*k[7]*k[11]-X[4]*X[9]*k[7]*k[18]+X[10]*k[7]*k[8]*(X[4]+X[9])-X[4]*X[9]*(k[7]*k[7])*(X[4]+X[9])-X[4]*X[8]*X[9]*k[7]*k[9]; 
mu[8] =  -k[8]*(X[10]*k[8]+X[10]*k[11]-X[4]*X[9]*k[7]); 
mu[9] =  X[7]*X[9]*k[5]*k[9]+X[8]*X[10]*k[8]*k[9]+X[9]*X[13]*k[9]*k[12]+X[11]*k[9]*k[10]*(X[8]+X[9])-X[8]*X[9]*(k[9]*k[9])*(X[8]+X[9])-X[4]*X[8]*X[9]*k[7]*k[9]; 
mu[10] =  -X[11]*(k[10]*k[10])+X[8]*X[9]*k[9]*k[10]; 
mu[11] =  -k[11]*(X[10]*k[8]+X[10]*k[11]-X[4]*X[9]*k[7]); 
mu[12] =  -X[13]*(k[12]*k[12]); 
mu[13] =  -k[13]*(-X[10]*k[11]+X[12]*k[13]+X[12]*k[14]); 
mu[14] =  -k[14]*(-X[10]*k[11]+X[12]*k[13]+X[12]*k[14]); 
mu[15] =  k[15]*(X[12]*k[13]-X[15]*k[15]+X[21]*k[20]-X[15]*X[20]*k[19]); 
mu[16] =  -X[16]*(k[16]*k[16])+X[12]*k[14]*k[16]; 
mu[17] =  k[17]*(X[3]*k[3]+X[10]*k[8]-X[4]*k[17]+X[10]*k[11]-X[4]*k[18]-X[4]*X[9]*k[7]); 
mu[18] =  k[18]*(X[3]*k[3]+X[10]*k[8]-X[4]*k[17]+X[10]*k[11]-X[4]*k[18]-X[4]*X[9]*k[7]); 
mu[19] =  X[12]*X[20]*k[13]*k[19]-X[15]*X[20]*k[15]*k[19]+X[15]*X[16]*k[19]*k[21]+X[20]*X[21]*k[19]*k[20]-X[15]*X[20]*(k[19]*k[19])*(X[15]+X[20]); 
mu[20] =  -X[21]*(k[20]*k[20])+X[15]*X[20]*k[19]*k[20]; 
mu[21] =  (X[12]*k[14]-X[16]*k[16])*k[21]; 


sigma2[0] =  (X[1]*X[1])*X[3]*(k[0]*k[0])*k[3]+(X[0]*X[0])*X[6]*(k[0]*k[0])*k[6]+X[0]*X[1]*(k[0]*k[0]*k[0])*pow(X[0]+X[1],2.0)+X[2]*(k[0]*k[0])*k[1]*pow(X[0]+X[1],2.0); 
sigma2[1] =  (k[1]*k[1])*(X[2]*k[1]+X[2]*k[2]+X[0]*X[1]*k[0]); 
sigma2[2] =  (k[2]*k[2])*(X[2]*k[1]+X[2]*k[2]+X[0]*X[1]*k[0]); 
sigma2[3] =  X[3]*(k[3]*k[3]*k[3])+X[2]*k[2]*(k[3]*k[3]); 
sigma2[4] =  X[5]*(k[4]*k[4]*k[4])+X[3]*k[3]*(k[4]*k[4]); 
sigma2[5] =  X[7]*(k[5]*k[5]*k[5]); 
sigma2[6] =  (k[6]*k[6])*(X[5]*k[4]+X[6]*k[6]+X[7]*k[5]); 
sigma2[7] =  X[3]*(X[9]*X[9])*k[3]*(k[7]*k[7])+(X[4]*X[4])*X[11]*(k[7]*k[7])*k[10]+(X[9]*X[9])*X[10]*(k[7]*k[7])*k[11]+X[4]*(X[9]*X[9])*(k[7]*k[7])*k[17]+X[4]*(X[9]*X[9])*(k[7]*k[7])*k[18]+X[4]*X[9]*(k[7]*k[7]*k[7])*pow(X[4]+X[9],2.0)+X[10]*(k[7]*k[7])*k[8]*pow(X[4]+X[9],2.0)+(X[4]*X[4])*X[8]*X[9]*(k[7]*k[7])*k[9]; 
sigma2[8] =  (k[8]*k[8])*(X[10]*k[8]+X[10]*k[11]+X[4]*X[9]*k[7]); 
sigma2[9] =  X[7]*(X[9]*X[9])*k[5]*(k[9]*k[9])+(X[8]*X[8])*X[10]*k[8]*(k[9]*k[9])+(X[9]*X[9])*X[13]*(k[9]*k[9])*k[12]+X[8]*X[9]*(k[9]*k[9]*k[9])*pow(X[8]+X[9],2.0)+X[11]*(k[9]*k[9])*k[10]*pow(X[8]+X[9],2.0)+X[4]*(X[8]*X[8])*X[9]*k[7]*(k[9]*k[9]); 
sigma2[10] =  X[11]*(k[10]*k[10]*k[10])+X[8]*X[9]*k[9]*(k[10]*k[10]); 
sigma2[11] =  (k[11]*k[11])*(X[10]*k[8]+X[10]*k[11]+X[4]*X[9]*k[7]); 
sigma2[12] =  X[13]*(k[12]*k[12]*k[12]); 
sigma2[13] =  (k[13]*k[13])*(X[10]*k[11]+X[12]*k[13]+X[12]*k[14]); 
sigma2[14] =  (k[14]*k[14])*(X[10]*k[11]+X[12]*k[13]+X[12]*k[14]); 
sigma2[15] =  (k[15]*k[15])*(X[12]*k[13]+X[15]*k[15]+X[21]*k[20]+X[15]*X[20]*k[19]); 
sigma2[16] =  X[16]*(k[16]*k[16]*k[16])+X[12]*k[14]*(k[16]*k[16]); 
sigma2[17] =  (k[17]*k[17])*(X[3]*k[3]+X[10]*k[8]+X[4]*k[17]+X[10]*k[11]+X[4]*k[18]+X[4]*X[9]*k[7]); 
sigma2[18] =  (k[18]*k[18])*(X[3]*k[3]+X[10]*k[8]+X[4]*k[17]+X[10]*k[11]+X[4]*k[18]+X[4]*X[9]*k[7]); 
sigma2[19] =  X[12]*(X[20]*X[20])*k[13]*(k[19]*k[19])+X[15]*(X[20]*X[20])*k[15]*(k[19]*k[19])+(X[15]*X[15])*X[16]*(k[19]*k[19])*k[21]+(X[20]*X[20])*X[21]*(k[19]*k[19])*k[20]+X[15]*X[20]*(k[19]*k[19]*k[19])*pow(X[15]+X[20],2.0); 
sigma2[20] =  X[21]*(k[20]*k[20]*k[20])+X[15]*X[20]*k[19]*(k[20]*k[20]); 
sigma2[21] =  (X[12]*k[14]+X[16]*k[16])*(k[21]*k[21]); 
} 

