# Set path to libraries and compilation flags

# Own libraries in ssm-libs
set SBML_INC_DIR=$HOME/ssm-libs/libSBML-5.8.0-Source/include
set SBML_LIB_DIR=$HOME/ssm-libs/libSBML-5.8.0-Source/lib

set BLITZ_INC_DIR=$HOME/ssm-libs/blitz-0.10
set BLITZ_LIB_DIR=$HOME/ssm-libs/blitz-0.10/lib

# LRZ libraries in /lrz/sys/libraries
set BOOST_INC_DIR=${BOOST_INCDIR}
set BOOST_LIB_DIR=${BOOST_LIBDIR}

set GSL_INC_DIR=${GSL_INCDIR}
set GSL_LIB_DIR=${GSL_LIBDIR}

echo BOOST_INC=${BOOST_INC}
echo GSL=${GSL_INCDIR}


#Compilation flags
set CPPFLAGS=(-I$SBML_INC_DIR -I$BLITZ_INC_DIR -L$BLITZ_LIB_DIR -lblitz -I$BOOST_INC_DIR -I$GSL_INC_DIR)

echo CPflags=$CPPFLAGS

echo --------------------------------------------------------------------------------------------------------------
echo ---------------------------------COMPILATION--ERRORS-AND-WARNINGS---------------------------------------------
echo --------------------------------------------------------------------------------------------------------------

g++ -O3 -w $CPPFLAGS -c ./source/RNGLib/*.cpp

echo --------------------------------------------------------------------------------------------------------------
echo ---------------------------------COMPILATION-TIMER-ERRORS-AND-WARNINGS---------------------------------------------
echo --------------------------------------------------------------------------------------------------------------

g++ -O3 $CPPFLAGS -c ./source/Timer/*.cpp

echo --------------------------------------------------------------------------------------------------------------
echo ---------------------------------COMPILATION-SBMLReaderAndParser-ERRORS-AND-WARNINGS---------------------------------------------
echo --------------------------------------------------------------------------------------------------------------

g++ -O3 $CPPFLAGS -c ./source/SBMLReaderAndParser.cpp

echo --------------------------------------------------------------------------------------------------------------
echo ---------------------------------COMPILATION-Simulation-ERRORS-AND-WARNINGS---------------------------------------------
echo --------------------------------------------------------------------------------------------------------------

g++ -O3 $CPPFLAGS -c ./source/Simulation.cpp

echo --------------------------------------------------------------------------------------------------------------
echo ---------------------------------COMPILATION-SSMReaction-ERRORS-AND-WARNINGS---------------------------------------------
echo --------------------------------------------------------------------------------------------------------------

g++ -O3 $CPPFLAGS -c ./source/SSMReaction.cpp

echo --------------------------------------------------------------------------------------------------------------
echo ---------------------------------COMPILATION-SSM-ERRORS-AND-WARNINGS---------------------------------------------
echo --------------------------------------------------------------------------------------------------------------

g++ -O3 $CPPFLAGS -c ./source/StochasticSimulationMethods.cpp 

echo --------------------------------------------------------------------------------------------------------------
echo ---------------------------------COMPILATION-Methods-ERRORS-AND-WARNINGS---------------------------------------------
echo --------------------------------------------------------------------------------------------------------------

g++ -O3 -w $CPPFLAGS -c ./source/Methods/*.cpp

echo --------------------------------------------------------------------------------------------------------------
echo --------------------------------------------------------------------------------------------------------------

echo COMPILE_SCRIPT_FINISHED

echo --------------------------------------------------------------------------------------------------------------
echo --------------------------------------------------------------------------------------------------------------






