#! /bin/csh -f
#
# 
# 
########## Set these paths to the base directories of the Blitz++, SBML, and BOOST libraries ############
set LOCAL_PATH=${HOME}/usr/local
set SVN_PATH=${HOME}/SVN

set BOOST_PATH=/usr/include/
set BLITZ_PATH=${LOCAL_PATH}/include/
set SBML_PATH=${LOCAL_PATH}/include/

echo BOOST_PATH = ${BOOST_PATH}
echo BLITZ_PATH = ${BLITZ_PATH}

ls ${BLITZ_PATH}

#########################################################################################################

echo --------------------------------------------------------------------------------------------------------------
echo ---------------------------------COMPILATION--ERRORS-AND-WARNINGS---------------------------------------------
echo --------------------------------------------------------------------------------------------------------------

g++ -O3 -I${SVN_PATH}/SSM -I${BOOST_PATH} -I${BLITZ_PATH} -I${SBML_PATH} -c ./source/RNGLib/*.cpp 

echo --------------------------------------------------------------------------------------------------------------
echo ---------------------------------COMPILATION-TIMER-ERRORS-AND-WARNINGS---------------------------------------------
echo --------------------------------------------------------------------------------------------------------------
g++ -O3 -I${SVN_PATH}/SSM -I${BOOST_PATH} -I${BLITZ_PATH} -I${SBML_PATH} -c ./source/Timer/*.cpp

echo --------------------------------------------------------------------------------------------------------------
echo ---------------------------------COMPILATION-SBMLReaderAndParser-ERRORS-AND-WARNINGS---------------------------------------------
echo --------------------------------------------------------------------------------------------------------------

g++ -O3 -I${SVN_PATH}/SSM -I${BOOST_PATH} -I${BLITZ_PATH} -I${SBML_PATH} -c ./source/SBMLReaderAndParser.cpp
echo --------------------------------------------------------------------------------------------------------------
echo ---------------------------------COMPILATION-Simulation-ERRORS-AND-WARNINGS---------------------------------------------
echo --------------------------------------------------------------------------------------------------------------

g++ -O3 -I${SVN_PATH}/SSM -I${BOOST_PATH} -I${BLITZ_PATH} -I${SBML_PATH} -c ./source/Simulation.cpp
echo --------------------------------------------------------------------------------------------------------------
echo ---------------------------------COMPILATION-SSMReaction-ERRORS-AND-WARNINGS---------------------------------------------
echo --------------------------------------------------------------------------------------------------------------

g++ -O3 -I${SVN_PATH}/SSM -I${BOOST_PATH} -I${BLITZ_PATH} -I${SBML_PATH} -c ./source/SSMReaction.cpp
echo --------------------------------------------------------------------------------------------------------------
echo ---------------------------------COMPILATION-SSM-ERRORS-AND-WARNINGS---------------------------------------------
echo --------------------------------------------------------------------------------------------------------------

g++ -O3 -I${SVN_PATH}/SSM -I${BOOST_PATH} -I${BLITZ_PATH} -I${SBML_PATH} -c ./source/StochasticSimulationMethods.cpp 
echo --------------------------------------------------------------------------------------------------------------
echo ---------------------------------COMPILATION-Methods-ERRORS-AND-WARNINGS---------------------------------------------
echo --------------------------------------------------------------------------------------------------------------

g++ -O3 -I${SVN_PATH}/SSM -I${BOOST_PATH} -I${BLITZ_PATH} -I${SBML_PATH} -c ./source/Methods/*.cpp

echo --------------------------------------------------------------------------------------------------------------
echo --------------------------------------------------------------------------------------------------------------

echo COMPILE_SCRIPT_FINISHED

echo --------------------------------------------------------------------------------------------------------------
echo --------------------------------------------------------------------------------------------------------------






